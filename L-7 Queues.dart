import 'dart:collection';

void main() {
// Creating a Queue
  Queue<String> geek = new Queue<String>();

// Printing default
// value of queue
  print(geek);

// Adding elements in a Queue
  geek.add("Geeks");
  geek.add("For");
  geek.add("Geeks");

// Printing the
// inserted elements
  print(geek);
}
